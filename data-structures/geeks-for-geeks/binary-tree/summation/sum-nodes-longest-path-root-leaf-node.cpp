class Solution
{
public:
    int sumOfLongRootToLeafPath(Node *root) {
        int level;
        return sumOfLongRootToLeafPath(root, level);
    }

    // time: O(V), memory: O(V)
    int sumOfLongRootToLeafPath(Node *root, int &level) {
        if (!root) {
            level = 0;
            return 0;
        }

        int level_1, level_2; 
        int s1 = sumOfLongRootToLeafPath(root -> left, level_1);
        int s2 = sumOfLongRootToLeafPath(root -> right, level_2);
        if (level_1 > level_2) {
            level = level_1 + 1;
            return s1 + root -> data;
        } else if (level_1 == level_2) {
            level = level_1 + 1;
            int max = (s1 > s2) ? s1 : s2;
            return max + root -> data;
        } else {
            level = level_2 + 1;
            return s2 + root -> data;
        }
    }
};

